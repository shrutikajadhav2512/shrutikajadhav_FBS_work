# Day of week
# take input no for day(1 to 7)
day=int(input('Enter number(1 to 7) for day:'))
if(day==1):
    print(f'{day}:Sunday')
elif(day==2):
    print(f'{day}:Monday')
elif(day==3):
    print(f'{day}:Tuesday')
elif(day==4):
    print(f'{day}:Wednesday')
elif(day==5):
    print(f'{day}:Thursday')
elif(day==6):
    print(f'{day}:Friday')
elif(day==7):
    print(f'{day}:Saturday')
else:
    print('Enter number 1 to 7')
